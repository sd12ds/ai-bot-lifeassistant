def main():
    pg = psycopg2.connect(PG_DSN)
    pgc = pg.cursor()

    # Получаем существующие имена
    pgc.execute("SELECT lower(name) FROM food_items WHERE user_id IS NULL")
    existing = {row[0] for row in pgc.fetchall()}

    inserted = 0
    skipped = 0

    for name, kcal, protein, fat, carbs in FOODS:
        if name.lower() in existing:
            skipped += 1
            continue
        pgc.execute(
            "INSERT INTO food_items (user_id, name, calories, protein_g, fat_g, carbs_g)"
            " VALUES (NULL, %s, %s, %s, %s, %s)",
            (name, kcal, protein, fat, carbs)
        )
        existing.add(name.lower())
        inserted += 1

    pg.commit()
    pgc.execute("SELECT count(*) FROM food_items WHERE user_id IS NULL")
    total = pgc.fetchone()[0]
    pg.close()
    print(f"Seed: вставлено={inserted}, пропущено={skipped}, всего системных={total}")


if __name__ == "__main__":
    main()
