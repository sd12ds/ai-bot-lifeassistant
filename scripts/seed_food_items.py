async def main():
    """Наполняет справочник продуктов. Идемпотентный — не дублирует."""
    inserted = 0
    skipped = 0
    async with AsyncSessionLocal() as session:
        for name, kcal, protein, fat, carbs in FOODS:
            result = await session.execute(
                select(FoodItem).where(
                    FoodItem.name == name,
                    FoodItem.user_id.is_(None),
                )
            )
            if result.scalar_one_or_none():
                skipped += 1
                continue
            session.add(FoodItem(
                user_id=None, name=name, calories=kcal,
                protein_g=protein, fat_g=fat, carbs_g=carbs,
                fiber_g=None, barcode=None,
            ))
            inserted += 1
        await session.commit()
    print(f"Seed: вставлено {inserted}, пропущено {skipped}, всего {inserted + skipped}")

if __name__ == "__main__":
    asyncio.run(main())
